"""OpenClaw Anomaly — Genome Assembler (ClawdBot Bridge)."""
from __future__ import annotations
from pathlib import Path
from openclaw.config import Config
import os, shutil

HEADER = "# GENERATED BY OGE — DO NOT EDIT DIRECTLY\n# Assembled: {timestamp}\n\n"

def read_genome_module(variant_path: Path, module_name: str, base_path: Path) -> str | None:
    """Read a module from variant dir, fall back to base genome/.
    Returns None if module doesn't exist anywhere (skip gracefully)."""
    variant_file = variant_path / module_name
    if variant_file.exists():
        return variant_file.read_text(encoding="utf-8")
    base_file = base_path / module_name
    if base_file.exists():
        return base_file.read_text(encoding="utf-8")
    return None

def assemble_soul(variant_path: Path | str, output_path: Path | str | None = None) -> str:
    """Concatenate all genome modules from variant into a single string.
    Falls back to base genome/ per module. Skips missing modules.
    Returns the assembled content."""
    variant_path = Path(variant_path)
    base_path = Config.GENOME_DIR
    sections = []
    for module in Config.GENOME_MODULES:
        content = read_genome_module(variant_path, module, base_path)
        if content is not None:
            separator = f"\n\n---\n\n## [{module}]\n\n"
            sections.append(separator + content)
    from datetime import datetime, timezone
    header = HEADER.format(timestamp=datetime.now(timezone.utc).isoformat())
    assembled = header + "".join(sections)
    if output_path:
        write_soul(assembled, Path(output_path))
    return assembled

def write_soul(content: str, output_path: Path | None = None) -> Path:
    """Write assembled SOUL.md atomically with backup."""
    path = output_path or Config.SOUL_MD_PATH
    # Backup existing
    if path.exists():
        bak = path.with_suffix(".md.bak")
        shutil.copy2(str(path), str(bak))
    # Atomic write
    tmp = path.with_suffix(".md.tmp")
    tmp.write_text(content, encoding="utf-8")
    os.replace(str(tmp), str(path))
    return path

def assemble_and_write(variant_path: Path | str) -> Path:
    """Assemble + write in one call. Returns SOUL.md path."""
    content = assemble_soul(variant_path)
    return write_soul(content)
