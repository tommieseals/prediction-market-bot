"""Tests for openclaw.genome_assembler — SOUL.md assembly pipeline."""
import os
import tempfile
import unittest
from pathlib import Path
from unittest.mock import patch

from openclaw.genome_assembler import assemble_soul, write_soul


class TestGenomeAssembler(unittest.TestCase):
    """Test genome_assembler.assemble_soul and write_soul.

    genome_assembler.py lives at openclaw/genome_assembler.py.
    assemble_soul(variant_path) reads modules from variant dir,
    falls back to base genome/, skips missing modules.
    """

    def setUp(self):
        self.tmp = tempfile.mkdtemp()
        self.variant_dir = Path(self.tmp) / "variant"
        self.variant_dir.mkdir()
        self.genome_dir = Path(self.tmp) / "genome"
        self.genome_dir.mkdir()
        self.soul_path = Path(self.tmp) / "SOUL.md"

    def test_assemble_soul_produces_content_with_header(self):
        """assemble_soul should return content with the GENERATED header."""
        (self.genome_dir / "loyalty_core.md").write_text("# Loyalty Core\nI serve Rusty.\n")

        with patch("openclaw.config.Config.GENOME_DIR", self.genome_dir), \
             patch("openclaw.config.Config.GENOME_MODULES", ["loyalty_core.md"]):
            content = assemble_soul(self.variant_dir)

        self.assertIn("GENERATED BY OGE", content)
        self.assertIn("Loyalty Core", content)

    def test_falls_back_to_base_genome_on_missing_variant_module(self):
        """If module is missing from variant dir, fall back to base genome/."""
        (self.genome_dir / "loyalty_core.md").write_text("# Base Loyalty\n")
        # variant_dir has no loyalty_core.md -- should fall back

        with patch("openclaw.config.Config.GENOME_DIR", self.genome_dir), \
             patch("openclaw.config.Config.GENOME_MODULES", ["loyalty_core.md", "nonexistent.md"]):
            content = assemble_soul(self.variant_dir)

        self.assertIn("Base Loyalty", content)
        # nonexistent.md should be skipped, not crash
        self.assertNotIn("nonexistent", content)

    def test_write_soul_backs_up_existing(self):
        """write_soul should create .bak of existing SOUL.md before overwrite."""
        self.soul_path.write_text("old content")
        bak_path = self.soul_path.with_suffix(".md.bak")

        write_soul("new content", output_path=self.soul_path)

        self.assertTrue(bak_path.exists())
        self.assertEqual(bak_path.read_text(), "old content")
        self.assertEqual(self.soul_path.read_text(), "new content")


if __name__ == "__main__":
    unittest.main()
